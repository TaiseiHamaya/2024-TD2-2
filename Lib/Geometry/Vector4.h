#pragma once

////////////////////////////////////////////////////////////////////////////////////////////
// Vector4 class
////////////////////////////////////////////////////////////////////////////////////////////
template <typename T>
class Vector4 { //!< xyzw, rgba
public:

	//=========================================================================================
	// public variables
	//=========================================================================================

	Vector4() = default;
	Vector4(T _x, T _y, T _z) : x(_x), y(_y), z(_z) {}
	Vector4(T _r, T _g, T _b, T _a) : r(_r), g(_g), b(_b), a(_a) {}

	union {
		T x, r;
	};

	union {
		T y, g;
	};

	union {
		T z, b;
	};

	union {
		T w, a = 1.0f;
	};

};

////////////////////////////////////////////////////////////////////////////////////////////
// using
////////////////////////////////////////////////////////////////////////////////////////////

using Vector4f = Vector4<float>;
using Vector4i = Vector4<int>;