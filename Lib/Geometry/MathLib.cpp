Vector4f Lerp(const Vector4f& x, const Vector4f& y, float s) {
	return {
		x.x + s * (y.x - x.x),
		x.y + s * (y.y - x.y),
		x.z + s * (y.z - x.z),
		x.w + s * (y.w - x.w)
	};
}

